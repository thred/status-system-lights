package io.github.thred.statussystemlights;

public class TrafficLight
{

    private String name;
    private Color color;

    public TrafficLight()
    {
        super();
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public Color getColor()
    {
        return color;
    }

    public void setColor(Color color)
    {
        this.color = color;
    }

}
